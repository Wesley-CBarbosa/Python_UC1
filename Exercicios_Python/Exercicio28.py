# Exercício 28: Crie um programa que solicite 5 números ao usuário e exiba a soma deles.

print("Digite 5 números e veja a soma deles.")

a = int(input("\nDigite o primeiro número: \n"))
b = int(input("\nDigite o segundo número: \n"))
c = int(input("\nDigite o terceiro número: \n"))
d = int(input("\nDigite o quarto número: \n"))
e = int(input("\nDigite o quinto número: \n"))

soma = a + b + c + d + e

print(f"A soma total dos valores é:", soma)